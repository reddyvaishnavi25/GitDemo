
public class Bus {
private int busId;
private String busBrand;

public Bus(int busId, String busBrand) {
	super();
	this.busId = busId;
	this.busBrand = busBrand;
}
public int getBusId() {
	return busId;
}
public void setBusId(int busId) {
	this.busId = busId;
}
public String getBusBrand() {
	return busBrand;
}
public void setBusBrand(String busBrand) {
	this.busBrand = busBrand;
}

}
